export const LanguageContext = createContext<any>(undefined);

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    return {
      lang: 'en' as Language,
      setLang: () => {},
      t: translations.en
    };
  }
  return context;
};
